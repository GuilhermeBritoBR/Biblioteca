function buscarLivros() {
    const termo = document
        .getElementById("search")
        .value
        .toLowerCase()
        .trim();

    const resultado = catalogo.filter(livro =>
        String(livro.ID).includes(termo) ||
        (livro.Autor || "").toLowerCase().includes(termo) ||
        (livro.Livro || "").toLowerCase().includes(termo) ||
        (livro.Setor || "").toLowerCase().includes(termo) ||
        (livro["Área"] || "").toLowerCase().includes(termo)
    );

        exibirLivros(resultado);
}

function contarAutores(lista = catalogo) {

    const autores = new Set();

    lista.forEach(livro => {

        if (livro.Autor) {
            autores.add(livro.Autor.trim());
        }

    });

    return autores.size;
}

function animarContador(id, valor) {

    const elemento = document.getElementById(id);

    if (!elemento) return;

    let atual = 0;

    const duracao = 1200;
    const fps = 60;

    const incremento = Math.max(1, Math.ceil(valor / (duracao / (1000 / fps))));

    const intervalo = setInterval(() => {

        atual += incremento;

        if (atual >= valor) {
            atual = valor;
            clearInterval(intervalo);
        }

        elemento.textContent = atual.toLocaleString("pt-BR");

    }, 1000 / fps);

}


